package com.itheima;

/*
    Java程序中最基本的组成单位是类 class

    类的格式定义是：
        public class 类名{

        }
 */
public class HelloWorld {
    /*
        这是main方法
        main方法是程序的入口方法，代码执行是从main方法开始的
     */
    public static void main(String[] args) {
        //这是输出语句，能够将""里面的内容输出到控制台，并且""里面的内容可以修改
        System.out.println("HelloWorld");
        System.out.println("java");
        System.out.println("阿基维利，再度启程吧，阿基维利");
        System.out.println("在 Java 中，包名不能以数字开头（模块4下的11）");
        System.out.println("右击11，Refactor，Rename，改为eleven");
        System.out.println("也可以直接选中11，按 Shift + F6 快捷键，改为eleven");
    }
}
