/*
编写 Java 代码的三个步骤
1. 编写代码 : 将代码写到 .java的源文件中
2. 编译代码 : 使用javac.exe(编译工具)对源文件进行编译, 从而产生.class字节码文件
				使用方式: javac Xxx.java
3. 运行代码 : 使用java.exe(运行工具)运行字节码文件
				使用方式: java Xxx
				运行的时候, 不要带后缀.class
两个工具的使用, 需要在DOS中, 通过指令进行使用.
 */